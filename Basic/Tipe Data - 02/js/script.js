// angka tanpa desimal
console.log(10);
console.log(1500);
console.log(123456);
// akurat sampai 15 digit

// angka dengan desimal
console.log(3.14);
console.log(0.5);
console.log(100.00);

// penjumlahan
console.log(0.3 + 0.7); //menjadi bilangan bulat
console.log(0.2 + 0.1); //masih desimal

// eksponen
console.log(123e5);
console.log(123e-5);

// // akan dianggap bilangan okta
// console.log(022);

// akan dianggap bilangan hexadesimal
console.log(0xFF);

// angka spesial

console.log(2/0);
console.log(-2/0);
console.log(0/0);
