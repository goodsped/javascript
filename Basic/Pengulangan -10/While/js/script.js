// while 
var angka = 1;
while(angka <= 10){
   console.log('Hello World! menggunakan while '+angka+" kali");
    angka++;
}

var ulang = true;
while(ulang){
    alert('Hello World!');
    ulang = confirm('coba lagi?'); 
}

var number = 1;
// do while 
do{
    console.log('Hello World! menggunakan do while '+ number+" kali");
    number++
}while(number <= 10);

